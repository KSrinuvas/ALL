#!/usr/bin/python3

##lambda function is used for single statement it will perform arthamatic operation

sum = lambda var1,var2:var1+var2;

print("sum of two no's :{}".format(sum(10,20)))
mul = lambda var1,var2:var1*var2;
print("mul of two no's :{}".format(mul(10,20)))
div = lambda var1,var2:var1/var2;
print("div of two no's :{}".format(div(10,20)))
sub = lambda var1,var2:var1+var2;
print("sub of two no's :{}".format(sub(10,20)))




